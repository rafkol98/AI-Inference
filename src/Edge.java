public class Edge {

    private Node first;  // first node.
    private Node second;  // Second node.

    /**
     * Create a new Edge.
     * @param first the first node in the edge connection.
     * @param second the second node in the edge connection.
     */
    public Edge(Node first, Node second) {
        this.first = first;
        this.second = second;
    }


}
